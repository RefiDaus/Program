#kondisi if adalah kondisi yang akan di eksekusi oleh program jika bernilai benar atau true

nilai = 9

#jika kondisi benar/true maka program akan mengeksekusi printah di bawah
if(nilai > 5) :
    print("sepuluh lebih besar dari angka lima") # kondisi benar, dieksekusi

#jika kondisi salah/false maka program tidak akan mengeksekusi printah di bawah
if(nilai > 10) :
    print("sembilan lebih besar dari angka sepuluh") # kondisi salah mak tidak tereksekusi